
# ### **Operators in Python**

# - **Arithmetic Operators:**
#   - `+` : Addition
#   - `-` : Subtraction
#   - `*` : Multiplication
#   - `/` : Division (results in float)
#   - `//` : Floor Division (integer division)
#   - `%` : Modulus (remainder)
#   - `**` : Exponentiation (power)



#-------------------------------------------------------------------------------------------------------------


#   - `+` : Addition


a = 5 
b = 3 
result =  5 + 8
print(result)


#   - `-` : Subtraction


a = 5 
b = 3 
result =  5 - 8
print(result)



#   - `*` : Multiplication


a = 5 
b = 3 
result =  5 * 8
print(result)


#   - `/` : Division (results in float)

a = 5 
b = 3 
result =  5 / 8
print(result)

#   - `//` : Floor Division (integer division)


a = 5 
b = 3 
result =  5 // 8
print(result)



#   - `%` : Modulus (remainder)

a = 5 
b = 3 
result =  5 % 8
print(result)


#   - `**` : Exponentiation (power)


a = 5 
b = 3 
result =  5 ** 8
print(result)

# -------------------------------------------------------------------------------------------------




# - **Comparison Operators:**
#   - `==` : Equal to
#   - `!=` : Not equal to
#   - `>` : Greater than
#   - `<` : Less than
#   - `>=` : Greater than or equal to
#   - `<=` : Less than or equal to


#------------------------------------------------------------------------------------------------------------


#   - `==` : Equal to

a = 15
b = 31 

result = a == b
print(result)


#   - `!=` : Not equal to

a = 15
b = 31 

result = a != b
print(result)

#   - `>` : Greater than

a = 15
b = 31 

result = a > b
print(result)



#   - `<` : Less than

a = 15
b = 31 

result = a < b
print(result)


#   - `>=` : Greater than or equal to

a = 15
b = 31 

result = a >= b
print(result)


#   - `<=` : Less than or equal to


a = 15
b = 31 

result = a <= b
print(result)


# ------------------------------------------------------------------------------------------------------------

# - **Logical Operators:**
#   - `and` : Logical AND
#   - `or` : Logical OR
#   - `not` : Logical NOT


#-----------------------------------------------------------------------------------------------------------




#   - `and` : Logical AND

result = (5 > 3) and (2 < 4)
print(result)


#   - `or` : Logical OR

result = (5 > 3) or (2 < 4)
print(result)



#   - `not` : Logical NOT

result = not (5 > 3) 
print(result)


#-----------------------------------------------------------------------------------------------------------

# - **Assignment Operators:**
#   - `=` : Assign
#   - `+=` : Add and assign
#   - `-=` : Subtract and assign
#   - `*=` : Multiply and assign
#   - `/=` : Divide and assign
#   - `//=` : Floor divide and assign
#   - `%=` : Modulus and assign
#   - `**=` : Exponentiate and assign


#----------------------------------------------------------------------------------------------------------


#   - `=` : Assign

x = 5 
print(x)


#   - `+=` : Add and assign

x = 5 
x += 1
print(x)



#   - `-=` : Subtract and assign

x = 5 
x -= 1
print(x)



#   - `*=` : Multiply and assign

x = 5 
x *= 100
print(x)



#   - `/=` : Divide and assign

x = 5 
x /= 100
print(x)



#   - `//=` : Floor divide and assign

x = 5 
x //= 100
print(x)


#   - `%=` : Modulus and assign

x = 5 
x %= 100
print(x)


#   - `**=` : Exponentiate and assign

