# --------------------------------------------------------------------------------------------------------


# - **Bitwise Operators:**
#   - `&` : Bitwise AND
#   - `|` : Bitwise OR
#   - `^` : Bitwise XOR
#   - `~` : Bitwise NOT
#   - `<<` : Bitwise left shift
#   - `>>` : Bitwise right shift


# ------------------------------------------------------------------------------------------------------

#   - `&` : Bitwise AND

x = 5 & 3 
print(x)


#   - `|` : Bitwise OR

x = 5 | 3 
print(x)

#   - `^` : Bitwise XOR

x = 5 ^ 3 
print(x)

#   - `~` : Bitwise NOT

x = 5 & 3 
print( ~x)

#   - `<<` : Bitwise left shift

x = 5 << 1
print(x)


#   - `>>` : Bitwise right shift

x = 5 >> 1
print(x)


# -------------------------------------------------------------------------------------------------------


# - **Membership Operators:**
#   - `in` : Checks if a value is present in a sequence
#   - `not in` : Checks if a value is not present in a sequence


#--------------------------------------------------------------------------------------------------------

#   - `in` : Checks if a value is present in a sequence

x = 5 in [1, 2, 3, 5]
print(x)


#   - `not in` : Checks if a value is not present in a sequence

x = 9 not in [1, 2, 3, 5]
print(x)


#-----------------------------------------------------------------------------------------------------


# - **Identity Operators:**
#   - `is` : Checks if two variables refer to the same object
#   - `is not` : Checks if two variables do not refer to the same object


#  ---------------------------------------------------------------------------------------------------

#   - `is` : Checks if two variables refer to the same object

x = a is b
print(x)



#   - `is not` : Checks if two variables do not refer to the same object

x = a is not b
print(x)




